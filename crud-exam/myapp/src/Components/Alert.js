import { toast , Flip} from 'react-toastify';

export function Waring_Alert (text) {
    toast.warn(text, {
        position: "top-right",
        autoClose: 2000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "colored",
        transition: Flip,
    });
}

